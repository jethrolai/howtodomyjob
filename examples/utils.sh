check_positive_float() {
  # integer
  echo "$1" | grep "^[0-9]*$"
  local val=`echo $?`
  if [[ $val == 0 ]]
  then
    return 1
  fi
  # float
  echo "$1" | grep "^[0-9]*[.][0-9]*$"
  local val=`echo $?`
  if [[ $val == 0 ]]
  then
    return 1
  else
    return 0
  fi
}
